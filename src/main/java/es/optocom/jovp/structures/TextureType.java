package es.optocom.jovp.structures;

/**
 * TextureType
 *
 * @since 0.0.1
 */
public enum TextureType {
  /** No texture (or flat) */
  FLAT,
  /** Checkerboard */
  CHECKERBOARD,
  /** Sine */
  SINE,
  /** Square sine */
  SQUARESINE,
  /** Gaussian 1st derivative */
  G1,
  /** Gaussian 2nd derivative */
  G2,
  /** Gaussian 3rd derivative */
  G3,
  /** Text */
  TEXT,
  /** Image */
  IMAGE
}
